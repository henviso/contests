#define MOD 1000000007LL

ll fastExp(ll base, ll p, ll m){
	if(p == 0) return 1;
	else if(p == 1) return base%m;
	else{
		unsigned long long res = fastExp(base, p/2, m);
		res = (res*res)%m;
		if(p%2 == 1) res = (res*base)%m;
		return res;
	}
}

