ll t, N, K, S;
ll A[1000100];
ll ans;

pair<ll, ll> w[5000100];

void SlidingWindow(){
	int front = 0, back = 0;
	for(ll i = 0; i<N; i++){
		while(!(front >= back) && w[back-1].first >= -A[i]) back--;
		w[back++] = pair<ll, ll>(-A[i], i);
		while(w[front].second <= i - K) front++;
		if(i + 1 >= K){
			ans += w[front].first;
		}
	}
	
}

void geraArray(){
	A[0] = S;
	for(ll i = 1LL; i < N; ++i)
    A[i] = (1LL*A[i-1]*1103515245LL + 12345LL) % (2147483648LL);
}

